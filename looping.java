public class looping {
    public static void main(String [] args){
        for(int i = 1; i<5;i++){
            System.out.println("hello");
        }
        int i = 0;
        while(i<5){
            System.out.println("helo");
            i++;
        }
        do{
            System.out.println("hi");
            i++;
        }while(i<5);
    }
}
//while and for loops are ENTRY CHECKUP loop
//do while are EXIT CHECKUP loop